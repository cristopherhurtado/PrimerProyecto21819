
public class Vehiculo {
    
    String patente;
    String modelo;
    int potencia;

    public Vehiculo(String patente, String modelo, int potencia) {
        this.patente = patente;
        this.modelo = modelo;
        this.potencia = potencia;
    }

    public String getPatente() {
        return patente;
    }

    public String getModelo() {
        return modelo;
    }

    public int getPotencia() {
        return potencia;
    }


    
    
    
}
